package odev3;

public class Instructor extends User {
	String detail;
	String giveCourse;
	
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getGiveCourse() {
		return giveCourse;
	}
	public void setGiveCourse(String giveCourse) {
		this.giveCourse = giveCourse;
	}

}
